<template>
  <div class="app-wrapper topics">
    <el-row class="search-wrapper">
      <el-col :span="8">
        <el-input v-model="searchValue" size="small" :placeholder="$t('Topics.topic')"></el-input>
      </el-col>
      <el-button type="primary" icon="el-icon-search" size="small" @click="handleSearch">
        {{ $t('Clients.search') }}
      </el-button>

      <el-table :data="tableData">
        <el-table-column prop="topic" :label="$t('Topics.topic')" sortable></el-table-column>
        <el-table-column prop="node" :label="$t('Clients.node')" sortable></el-table-column>
      </el-table>
    </el-row>

    <div class="emq-table-footer">
      <el-pagination
        v-if="count > 0"
        layout="total, sizes, prev, pager, next"
        :page-sizes="[20, 50, 100, 500]"
        :page-size.sync="params.limit"
        :current-page.sync="params.page"
        :total="count"
        @size-change="handleSizeChange"
        @current-change="handleCurrentPageChange"
      >
      </el-pagination>
    </div>
  </div>
</template>

<script>
import { listTopics, searchTopics } from '@/api/topics'

export default {
  name: 'Topics',

  data() {
    return {
      tableData: [],
      searchValue: '',
      params: {
        page: 1,
        limit: 20,
      },
      count: 0,
    }
  },

  created() {
    this.loadData()
  },

  methods: {
    async handleSearch() {
      const topic = this.searchValue.trim()
      if (!topic) {
        this.loadTopics()
        return
      }
      this.params.page = 1
      this.count = 0
      this.tableData = await searchTopics(topic)
    },
    handleSizeChange() {
      this.loadTopics(true)
    },
    handleCurrentPageChange() {
      this.loadTopics()
    },
    async loadData() {
      this.loadTopics()
    },
    async loadTopics(reload, params = {}) {
      if (reload) {
        this.params.page = 1
      }
      const respData = await listTopics({ ...this.params, ...params })
      const {
        data = [],
        meta: { count = 0 },
      } = respData
      this.tableData = data
      this.count = count
    },
  },
}
</script>
